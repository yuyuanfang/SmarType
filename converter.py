# 构建翻译表
_TABLE_CN_TO_TW = str.maketrans(_CN_CHARS, _TW_CHARS)
_TABLE_TW_TO_CN = str.maketrans(_TW_CHARS, _CN_CHARS)


def cn_to_tw(text: str) -> str:
    """简体中文 → 繁体中文"""
    # 先做词汇级替换
    for cn_word, tw_word in WORD_CN_TO_TW.items():
        text = text.replace(cn_word, tw_word)
    # 再做字符级替换
    return text.translate(_TABLE_CN_TO_TW)


def tw_to_cn(text: str) -> str:
    """繁体中文 → 简体中文"""
    # 先做词汇级替换
    for tw_word, cn_word in WORD_TW_TO_CN.items():
        text = text.replace(tw_word, cn_word)
    # 再做字符级替换
    return text.translate(_TABLE_TW_TO_CN)


def convert(text: str, target_lang: str) -> str:
    """
    转换到目标语言
    target_lang: "zh-TW" 或 "zh-CN" 或 "en"（英文不转换）
    """
    if target_lang == "zh-TW":
        return cn_to_tw(text)
    elif target_lang == "zh-CN":
        return tw_to_cn(text)
    else:
        return text  # 英文或其他，不转换


def detect_script(text: str) -> str:
    """
    粗略检测文字是简体还是繁体
    返回 "zh-CN" 或 "zh-TW" 或 "unknown"
    """
    tw_chars = set(_TW_CHARS)
    cn_chars = set(_CN_CHARS)

    tw_count = sum(1 for c in text if c in tw_chars)
    cn_count = sum(1 for c in text if c in cn_chars)

    if tw_count > cn_count:
        return "zh-TW"
    elif cn_count > tw_count:
        return "zh-CN"
    else:
        return "unknown"


